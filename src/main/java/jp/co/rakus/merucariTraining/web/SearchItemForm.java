package jp.co.rakus.merucariTraining.web;

public class SearchItemForm {

	private String name;
	private String parant;
	private String child;
	private String grandChild;
	private String brand;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getParant() {
		return parant;
	}
	public void setParant(String parant) {
		this.parant = parant;
	}
	public String getChild() {
		return child;
	}
	public void setChild(String child) {
		this.child = child;
	}
	public String getGrandChild() {
		return grandChild;
	}
	public void setGrandChild(String grandChild) {
		this.grandChild = grandChild;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}

}
